li = [2,45,3,12,67,4,8,98,5,66]
n = int(input("enter the number: "))
i = li[0]
j = li[-1]
while True:

    if i != n or j != n:
        i += 1
        j -= 1
        
        
    if i == n or i == j:
        print("in list")
    else:
        print("not in list")
    break
